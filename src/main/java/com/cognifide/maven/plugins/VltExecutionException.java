package com.cognifide.maven.plugins;

public class VltExecutionException extends RuntimeException {

	private static final long serialVersionUID = -4639309701219052464L;

	public VltExecutionException() {
		super();
	}

	public VltExecutionException(String message, Throwable cause) {
		super(message, cause);
	}

	public VltExecutionException(String message) {
		super(message);
	}

	public VltExecutionException(Throwable cause) {
		super(cause);
	}

}
